   public void init(Indigo indigo, IndigoRenderer indigo_renderer, 
                    RenderableObject rend_obj, boolean is_reaction)
   {
      this._indigo = indigo;
      this._indigo_renderer = indigo_renderer;
      this._obj = rend_obj.getObject();
      this._is_reaction = is_reaction;

      if (!_obj.hasCoord())
         _obj.layout();
      mol_view_panel.init(_indigo, _indigo_renderer);
      mol_view_panel.setMol(_obj);

      String mol_name = _obj.name();

      if (true || mol_name == null || mol_name.length() == 0)
      {
         try {
            mol_name = _obj.canonicalSmiles();
         } catch (Exception ex) {
            mol_name = null;
         }
      }

      _fillPropertyTable();

      mol_view_panel.setSize(mol_view_panel.getPreferredSize());

      setTitle(mol_name);

      // Set window position in the middle
      Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
      Dimension windowSize = getSize();
      int windowX = Math.max(0, (screenSize.width - windowSize.width) / 2);
      int windowY = Math.max(0, (screenSize.height - windowSize.height) / 2);

      setLocation(windowX, windowY);

      setVisible(true);
      setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
   }

   private void _fillPropertyTable()
   {
      DefaultTableModel model = (DefaultTableModel) property_table.getModel();
      while (model.getRowCount() != 0)
         model.removeRow(0);
      
      for (IndigoObject prop : _obj.iterateProperties())
      {
         String prop_name = prop.name();
         String prop_value = _obj.getProperty(prop.name());

         model.addRow(new String[] {prop_name, prop_value});
      }
   }

